    public void refreshLeds() {
        led1.setBackground(Color.GRAY);
        led2.setBackground(Color.GRAY);
        led3.setBackground(Color.GRAY);
        led4.setBackground(Color.GRAY);
        led5.setBackground(Color.GRAY);
        led6.setBackground(Color.GRAY);
        led7.setBackground(Color.GRAY);
        decimalValue = dsw1.getValue() + dsw2.getValue() * 2 + dsw3.getValue() * 4;
        switch (decimalValue) {
            case 1:
                led1.setBackground(Color.GREEN);
                break;
            case 2:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                break;
            case 3:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                led3.setBackground(Color.GREEN);
                break;
            case 4:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                led3.setBackground(Color.GREEN);
                led4.setBackground(Color.GREEN);
                break;
            case 5:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                led3.setBackground(Color.GREEN);
                led4.setBackground(Color.GREEN);
                led5.setBackground(Color.GREEN);
                break;
            case 6:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                led3.setBackground(Color.GREEN);
                led4.setBackground(Color.GREEN);
                led5.setBackground(Color.GREEN);
                led6.setBackground(Color.GREEN);
                break;
            case 7:
                led1.setBackground(Color.GREEN);
                led2.setBackground(Color.GREEN);
                led3.setBackground(Color.GREEN);
                led4.setBackground(Color.GREEN);
                led5.setBackground(Color.GREEN);
                led6.setBackground(Color.GREEN);
                led7.setBackground(Color.GREEN);
                break;
            default:
                break;
        }
    }

