    private void action() {
        
        /*
        int receiveDataBuffer[] = new int[9];  

        for (int i = 0; i < 9; i++) {
            receiveDataBuffer[i] = arduino_UNO.readData();
        }

        // Filling variables
        operational_mode = receiveDataBuffer[0] == 1;
        operational_control = receiveDataBuffer[1] == 1;
        int dsw_bit0 = receiveDataBuffer[2];
        int dsw_bit1 = receiveDataBuffer[3];
        int dsw_bit2 = receiveDataBuffer[4];
        decimal_value = receiveDataBuffer[5];
        int push_b1 = receiveDataBuffer[6];
        int push_b2 = receiveDataBuffer[7];
        int gate_result = receiveDataBuffer[8];
        */
        
        int gateResult = 0;
        if (operational_mode == BIN_MODE) {
            allLedsOFF();
            decimal_value = jSliderDSW1.getValue() + jSliderDSW2.getValue() * 2 + jSliderDSW3.getValue() * 4;
            switch (decimal_value) {
                case 1:
                    jPanelLed1.setBackground(Color.GREEN);
                    break;
                case 2:
                    jPanelLed1.setBackground(Color.GREEN);
                    jPanelLed2.setBackground(Color.GREEN);
                    break;
                case 3:
                    jPanelLed1.setBackground(Color.GREEN);
                    jPanelLed2.setBackground(Color.GREEN);
                    jPanelLed3.setBackground(Color.GREEN);
                    break;
                case 4:
                    jPanelLed1.setBackground(Color.GREEN);
                    jPanelLed2.setBackground(Color.GREEN);
                    jPanelLed3.setBackground(Color.GREEN);
                    jPanelLed4.setBackground(Color.GREEN);
                    break;
                case 5:
                    jPanelLed1.setBackground(Color.GREEN);
                    jPanelLed2.setBackground(Color.GREEN);
                    jPanelLed3.setBackground(Color.GREEN);
                    jPanelLed4.setBackground(Color.GREEN);
                    jPanelLed5.setBackground(Color.GREEN);
                    break;
                case 6:
                    jPanelLed1.setBackground(Color.GREEN);
                    jPanelLed2.setBackground(Color.GREEN);
                    jPanelLed3.setBackground(Color.GREEN);
                    jPanelLed4.setBackground(Color.GREEN);
                    jPanelLed5.setBackground(Color.GREEN);
                    jPanelLed6.setBackground(Color.GREEN);
                    break;
                case 7:
                    allLedsON();
                    break;
                default:
                    break;
            }
            // Send int value to arduino board
            if (arduino_UNO != null && (operational_control == BY_SOFTWARE)) {
                arduino_UNO.writeData(decimal_value);
            }
        } else if (operational_mode == GATES_MODE) {
            gate = jComboBoxGatesMenu.getSelectedIndex();
            switch (gate) {
                case 1: // NOT
                    gateResult = (button_input1 == 1) ? 0 : 1;
                    break;
                case 2: // AND
                    gateResult = button_input1 & button_input2;
                    break;
                case 3: // OR
                    gateResult = button_input1 | button_input2;
                    break;
                case 4: // NOR
                    gateResult = ((button_input1 | button_input2) == 1) ? 0 : 1;
                    break;
                case 5: // NAND
                    gateResult = ((button_input1 & button_input2) == 1) ? 0 : 1;
                    break;
                case 6: // XOR
                    gateResult = (button_input1 != button_input2) ? 1 : 0;
                    break;
                case 7: // XNOR
                    gateResult = (button_input1 == button_input2) ? 1 : 0;
                    break;
                default:
                    break;
            }

            // Define output value in Led1
            if (gateResult == 1) {
                jPanelLed1.setBackground(Color.GREEN);
            } else {
                jPanelLed1.setBackground(Color.GRAY);
            }
            if (arduino_UNO != null) {
                arduino_UNO.writeData(gateResult);
            }
        }
        //System.out.println(decimal_value);
    }

